// ============================================================================
// SPELL LOOKUPS
// ============================================================================

// Lookup by D&D name
export const SPELLS_BY_NAME: Record<string, Spell> = SPELLS.reduce(
    (acc, spell) => {
        acc[spell.name] = spell;
        return acc;
    },
    {} as Record<string, Spell>
);

// Lookup by Hollowgear name
export const SPELLS_BY_HOLLOWGEAR_NAME: Record<string, Spell> = SPELLS.reduce(
    (acc, spell) => {
        if (spell.hollowgearName) {
            acc[spell.hollowgearName] = spell;
        }
        return acc;
    },
    {} as Record<string, Spell>
);

// ============================================================================
// HELPER FUNCTIONS
// ============================================================================

// Get spells by level
export const getSpellsByLevel = (level: number): Spell[] => {
    return SPELLS.filter((spell) => spell.level === level);
};

// Get spells by class
export const getSpellsByClass = (classType: string): Spell[] => {
    return SPELLS.filter((spell) => spell.classes.includes(classType as any));
};

// Get cantrips (level 0)
export const CANTRIPS = getSpellsByLevel(0);

// Get level 1 spells
export const LEVEL_1_SPELLS = getSpellsByLevel(1);

